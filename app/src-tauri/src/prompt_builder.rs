impl PetPromptConfig {
    fn style_contract(&self) -> String {
        let base = "Desktop companion sprite: compact full-body character, readable in a 192x208 cell, clear silhouette, and crisp edges for chroma-key extraction.";
        let preset_desc = STYLE_CONTRACTS
            .iter()
            .find(|(k, _)| *k == self.style_preset)
            .map(|(_, v)| *v)
            .unwrap_or(STYLE_CONTRACTS[0].1);
        format!("{base} Style `{}`: {preset_desc}", self.style_preset)
    }
}

pub fn build_base_prompt(config: &PetPromptConfig) -> String {
    let style_contract = config.style_contract();
    format!(
        r#"Create one clean full-body reference sprite for a desktop companion named "{name}".

Character identity: {description}
Style: {style_contract}

IMPORTANT: Preserve the original character type exactly as described. Do NOT transform the character into a cat, animal, or any creature unless explicitly described as one. If the character is a robot, keep it a robot. If it is a humanoid, keep it humanoid. If it is an object, keep it as that object. If it is an animal, keep it as an animal.

Place a single centered pose on a perfectly flat pure {chroma} chroma-key background. Keep the full character visible, compact, readable at 192x208, and easy to animate. No scenery, text, borders, checkerboard transparency, shadows, glows, detached effects, or extra props. Keep {chroma} and close colors out of the character body."#,
        name = config.pet_name,
        description = config.description,
        style_contract = style_contract,
        chroma = config.chroma_key,
    )
}

pub fn build_row_prompt(config: &PetPromptConfig, row_index: usize) -> String {
    let spec = &ROW_SPECS[row_index];
    let style_contract = config.style_contract();
    format!(
        r#"Create one horizontal animation strip for desktop companion "{name}", state `{state}`.

Use the attached canonical base for identity. Use the attached layout guide only for slot count, spacing, centering, and padding; do not draw the guide.

Output exactly {frames} full-body frames in one left-to-right row on flat pure {chroma}. Treat the row as {frames} invisible equal-width slots: one centered complete pose per slot, evenly spaced, with no overlap, clipping, empty slots, labels, or borders.

IMPORTANT - Character preservation: Keep the EXACT same character type from the base image. Do NOT transform it into a cat, animal, or any other creature type. A robot stays a robot. A humanoid stays humanoid. An animal stays an animal. An object stays an object. Preserve silhouette, face, proportions, markings, palette, material, style, and all identifying features across every frame.

Identity: same character in every frame: {description}
Style: {style_contract}
Animation continuity: keep apparent character scale and baseline stable within the row unless the state intentionally changes vertical position (like `jumping`). Move the pose within the slot instead of redrawing the character larger or smaller frame to frame.

State action: {action}

State requirements:
{requirements}

Clean extraction: crisp opaque edges, safe padding, no scenery, text, guide marks, checkerboard, shadows, glows, motion blur, speed lines, dust, detached effects, stray pixels, or chroma-key colors inside the character."#,
        name = config.pet_name,
        state = spec.state,
        frames = spec.frames,
        chroma = config.chroma_key,
        description = config.description,
        style_contract = style_contract,
        action = spec.action,
        requirements = spec.requirements,
    )
}

/// Build prompt for a BATCH of frames within a row (for splitting wide strips).
pub fn build_batch_prompt(config: &PetPromptConfig, row_index: usize, batch_frames: u32, batch_start: u32, total_frames: u32) -> String {
    let spec = &ROW_SPECS[row_index];
    let style_contract = config.style_contract();
    let continuity_note = if batch_start == 0 {
        format!("This is the beginning of the animation (frames 1-{} of {}).", batch_frames, total_frames)
    } else {
        format!("This continues the animation (frames {}-{} of {}). Maintain the same pose progression as if extending the previous frames.", batch_start + 1, batch_start + batch_frames, total_frames)
    };
    format!(
        r#"Create one horizontal animation strip for desktop companion "{name}", state `{state}`.

Use the attached canonical base for identity. Use the attached layout guide only for slot count, spacing, centering, and padding; do not draw the guide.

Output exactly {batch_frames} full-body frames in one left-to-right row on flat pure {chroma}. Treat the row as {batch_frames} invisible equal-width slots: one centered complete pose per slot, evenly spaced, with no overlap, clipping, empty slots, labels, or borders.

{continuity_note}

IMPORTANT - Character preservation: Keep the EXACT same character type from the base image. Do NOT transform it into a cat, animal, or any other creature type. Preserve silhouette, face, proportions, markings, palette, material, style, and all identifying features across every frame.

Identity: same character in every frame: {description}
Style: {style_contract}
Animation continuity: keep apparent character scale and baseline stable within the row unless the state intentionally changes vertical position (like `jumping`). Move the pose within the slot instead of redrawing the character larger or smaller frame to frame.

State action: {action}

State requirements:
{requirements}

Clean extraction: crisp opaque edges, safe padding, no scenery, text, guide marks, checkerboard, shadows, glows, motion blur, speed lines, dust, detached effects, stray pixels, or chroma-key colors inside the character."#,
        name = config.pet_name,
        state = spec.state,
        batch_frames = batch_frames,
        chroma = config.chroma_key,
        continuity_note = continuity_note,
        description = config.description,
        style_contract = style_contract,
        action = spec.action,
        requirements = spec.requirements,
    )
}

pub fn row_count() -> usize {
    ROW_SPECS.len()
}

pub fn row_state_name(index: usize) -> &'static str {
    ROW_SPECS[index].state
}

pub fn row_frame_count(index: usize) -> u32 {
    ROW_SPECS[index].frames
}
