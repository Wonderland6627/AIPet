/// Prompt generation for pet sprite creation.
/// Prompts are designed to be generic — they work for any character type
/// (animals, robots, humanoids, game characters, personified objects, etc.)

pub struct PetPromptConfig {
    pub pet_name: String,
    pub description: String,
    pub style_preset: String,
    pub chroma_key: String,
}

struct RowSpec {
    state: &'static str,
    frames: u32,
    action: &'static str,
    requirements: &'static str,
}

